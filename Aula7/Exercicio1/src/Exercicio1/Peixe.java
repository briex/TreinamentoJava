package Exercicio1;

public class Peixe extends Animal {
	String caracteristica;
	
	public Peixe (String nome, String cor, int comprimento, float velocidade, String ambiente, String caracteristica)
	{
		super (nome, cor,comprimento, ambiente, velocidade);
		this.caracteristica = caracteristica;
	}
	
	void alteraCaracteristica(String caracteristica)
	{
		this.caracteristica = caracteristica;
	}
	
	public String caracteristica()
	{
		return caracteristica;
	}
	
	@Override
	public String dados()
	{
		return super.dados() + "\n Caracteristica: " + this.caracteristica;
	}


}
